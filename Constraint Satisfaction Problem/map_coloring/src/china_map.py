def build_adjacency_dict():
    """Return a dict mapping each region name to its set of neighbours."""
    adj = {name: set() for name, _ in REGIONS}
    for a, b in ADJACENCY:
        adj[a].add(b)
        adj[b].add(a)
    return adj
